export const getEventsByCategory = (category: string) => 
  COMPREHENSIVE_EVENTS.filter(event => event.category === category);

export const getEventsByDecade = (startYear: number) =>
  COMPREHENSIVE_EVENTS.filter(event => {
    const eventYear = parseInt(event.date.split(' ')[2]);
    return eventYear >= startYear && eventYear < startYear + 10;
  });

